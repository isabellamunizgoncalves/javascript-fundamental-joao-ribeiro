/**
 * Intrudução arrays:
 * No JS, como noutras LINGUAGENS DE PROGRAMAÇÃO, existe uma 
 * ESTRUTURA DE DADOS chamada: ARRAY(ou vetor) que é usada para ARMAZENAR uma 
 * COLEÇÃO DE VALORES.
 * 
 * Um ARRAY é uma LISTA DE VALORES que podem ser de qualquer TIPO, como:
 * NÚMEROS,
 * STRINGS,
 * OBJETOS
 * [Dentre outros]
 * 
 * Os ARRAYS são muito ÚTEIS para ARMAZENAR e MANIPULAR DADOS de forma EFICIENTE:
 */

//Exemplo de um ARRAY DE STRINGS:
let names = ['Isa', 'Rique', 'Rosesca'];

//Exemplo de um ARRAY DE NÚMEROS:
let numbers = [1,2,3,4,5];

//Também podemos ter os mesmos ARRAYS TIPADOS de uma forma diferente
let names2 = new Array('Isa', 'Rique', 'Rosesca');
let numbers2 = new Array(1,2,3,4,5);

