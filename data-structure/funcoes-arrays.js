// array de strings
let names = ['Isa', 'Mary', 'Bob'];

//adicionar um novo elemento ao array
names.push('Isaque'); //['Isa', 'Mary', 'Bob', 'Isaque']

//remove o último elemento do array
names.pop();//['Isa', 'Mary', 'Bob']

//adiciona um novo elemento ao início do array:
names.unshift('Isaque');

//remove o primeiro elemento do array
names.shift();

