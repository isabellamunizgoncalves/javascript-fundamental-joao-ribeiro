//os tipos de dados 'null' e 'undefined' são dois tipos de dados primitivos em JavaScript
//que representam a AUSÊNCIA DE VALOR ou a FALTA DE DEFINIÇÃO de uma VARIÁVEL

let myNull = null;

console.log(myNull); //undefined pode ser atribuído a uma variável, mas não é recomendado

let myUndefined = undefined;

console.log(myUndefined); //undefined
console.log(myUndefined);
//undefined significa que ainda não foi atribuído nenhum valor à variável

let myNumber; // a variável 'myNumber' não tem nenhum valor atribuído a ela

console.log(myNumber); //undefined

